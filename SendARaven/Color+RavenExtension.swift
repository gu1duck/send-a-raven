import Foundation

extension UIColor {
    class func raven() -> UIColor {
        return UIColor(red: 145/255, green: 89/255, blue: 1, alpha: 1)
    }
    
    class func ravenGreen() -> UIColor {
        return UIColor(red: 52/256, green: 213/256, blue: 112/256, alpha: 1)
    }
    class func ravenRed() -> UIColor {
        return UIColor(red: 245/256, green: 91/256, blue: 91/256, alpha: 1)
    }
    
}